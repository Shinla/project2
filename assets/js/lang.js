function changeLanguage(languageCode) {
    // Update menu items
    const menuItems = document.querySelectorAll('[data-translation-key]');
    menuItems.forEach(item => {
        const translationKey = item.getAttribute('data-translation-key');
        item.textContent = translations[languageCode][translationKey];
        
    });

    // Store the selected language in localStorage
    localStorage.setItem('selectedLanguage', languageCode);
}

// Function to set the language on page load
function setInitialLanguage() {
    // Retrieve the selected language from localStorage
    const selectedLanguage = localStorage.getItem('selectedLanguage');

    // Default to 'en' if no language is stored
    const languageCode = selectedLanguage || 'en';

    // Change the language on page load
    changeLanguage(languageCode);
}

// Call the setInitialLanguage function when the page loads
window.addEventListener('load', setInitialLanguage);

